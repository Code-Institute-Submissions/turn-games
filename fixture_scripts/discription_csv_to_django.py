def create_description(input_file='steam_description_data.csv'):
    """ finds and cleans the description from the description csv file"""
    data = {}
    with open(input_file) as f:
        reader = csv.DictReader(f)
        rows = list(reader)

        # Clean CSV File
        for row in rows:

            del row['detailed_description']
            del row['about_the_game']

            if row["steam_appid"] in str(used_ids):
                data[row['steam_appid']] = row['short_description']
    return data
